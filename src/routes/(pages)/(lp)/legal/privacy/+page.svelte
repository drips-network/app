      review decisions. This data is required to enable RPGF functionality. Most information is
      public, except the voter list, which is visible only to round admins and select platform
      maintainers for maintenance purposes.
    </p>

    <h5>As an RPGF applicant</h5>
    <p>
      If you submit an application for an RPGF round, we process your wallet address on supported
      networks, proof of project ownership (e.g. by committing a verification file to the relevant
