<h4>When using the Drips Protocol and Drips App:</h4>
<p>
  When You/a User sets up a token stream or transfer via the Drips Protocol (which can be done - but
  is not required - via the Drips App, app.drips.network), You/they will need to connect the token
  sender’s Ethereum address (likely Your/their own Ethereum address, i.e. Your/their public key).
  You/They will also need to indicate the Ethereum address that should be able to access and receive
  the sent tokens. This use of Ethereum addresses (public keys) is critical to ensure tokens
  streamed, shared, or transferred are accessed by the correct recipient. The use of Your Ethereum
  address - including via your interaction with the Drips Protocol - publishes data about your
  interaction to the Ethereum Network.
</p>
<p>
  When using the Drips App, there are many ways for You to generate public data, including but not
  limited to: topping up funds; collecting funds; creating streams (transfers); editing or deleting
  streams; creating a (Collaborative) Drip List; editing or transferring a Drip List; claiming a
  Project; and editing a Project. For example, when using the Drips App to create a token stream,
  the metadata a User gives the token stream or transfer - including the Ethereum addresses of the
  sender and recipient(s) You indicate, the name You give to the stream or transfer, the amount of
  tokens You transfer, and the time and frequency parameters You set for the token transfer - are
  published to and stored in the InterPlanetary File System (IPFS). Each piece of data stored in the
  IPFS network gets its own unique content identifier (CID), a hash of the metadata sent to IPFS,
  which is created by IPFS. The Drips App retrieves the CID of the published IPFS document (i.e. the
  User’s metadata), and then writes this CID on-chain (via the Drips Protocol) in association with
  the User’s Drips account. By design, details of any activity initiated and made by You via the
  Drips App are public information and are stored on the blockchain and anyone is able to download,
  access and view Your metadata (and any personal data embedded therein) stored in the IPFS if they
  have the CID.
</p>
<p>
