<script lang="ts">
  import FormField from '$lib/components/form-field/form-field.svelte';
  import TextInput from '$lib/components/text-input/text-input.svelte';
  import type {
    ApplicationUrlAnswerDto,
    ApplicationUrlField,
  } from '$lib/utils/rpgf/types/application';
  import type { ComponentProps } from 'svelte';

  export let field: ApplicationUrlField;
  export let answer: ApplicationUrlAnswerDto | undefined = undefined;
  export let valid: boolean = false;
  export let forceRevealError: boolean | undefined = undefined;

  let value: string | undefined = answer?.value;
  $: if (value) {
    answer = {
      fieldId: field.id,
      value: value,
    };
  } else {
    answer = undefined;
  }

  function isValidUrl(url: string): boolean {
    try {
      new URL(url);
      return true;
    } catch {
      return false;
    }
  }

  $: hasValidUrl = value !== undefined && value.trim() !== '' && isValidUrl(value);
  $: {
    if (field.required) {
      valid = hasValidUrl;
    } else if (value !== undefined && value.trim() !== '' && !isValidUrl(value)) {
      valid = false;
    } else {
      valid = true;
    }
  }

  let beenFocussed = false;
  let formFieldValidationState: ComponentProps<FormField>['validationState'];
  $: formFieldValidationState = valid
    ? { type: 'valid' }
    : beenFocussed || forceRevealError
      ? {
          type: 'invalid',
          message: 'This field is required and must be a full, valid URL, including https://.',
        }
      : { type: 'valid' };
</script>

<FormField
  title={field.label + (field.required ? '*' : '')}
  descriptionMd={field.descriptionMd}
  validationState={formFieldValidationState}
  privateNoticeText={field.private
    ? 'Data in this field is private and will only be shared with the admins of the round.'
    : undefined}
>
  <TextInput bind:value on:blur={() => (beenFocussed = true)} />
</FormField>
